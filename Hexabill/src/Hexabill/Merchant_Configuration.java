    private void tableDisplay(){
         
         int c;
         
          try {
            Class.forName("com.mysql.cj.jdbc.Driver");
    
            con1 = DriverManager.getConnection("jdbc:mysql://localhost/hexabilling","root",""); 
            
            insert = con1.prepareStatement("SELECT * FROM merchant_conf");
            
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData rsm = rs.getMetaData();
            
            c = rsm.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel)jTable1.getModel();
           
            DFT.setRowCount(0);
            while(rs.next()){
                Vector v2 = new Vector();
                
                for(int a=1;a<=c;a++){
                    v2.add(rs.getString("Merchant_ID"));
                    v2.add(rs.getString("Merchant_Name"));
                    v2.add(rs.getString("Address_1"));
                    v2.add(rs.getString("Address_2"));
                    v2.add(rs.getString("Address_3"));
                    v2.add(rs.getString("Address_4"));
                    v2.add(rs.getString("Address_5"));
                    v2.add(rs.getString("Phone"));
                    v2.add(rs.getString("Mobile"));
                    v2.add(rs.getString("Email"));
                    v2.add(rs.getString("Web"));
                }
                
                DFT.addRow(v2);
            }
     
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddProducts.class.getName()).log(Level.SEVERE, null, ex);
        }
 
     }
    
