    private void fetchMerchatname(){
        
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/hexabilling", "root", "");
            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT Merchant_Name FROM pos_metchant_name WHERE id = 1");

            if (resultSet.next()) {
                String merchantName = resultSet.getString("Merchant_Name");
                merchanttName.setText(merchantName);
            } else {
                merchanttName.setText("Merchant Name not found");
            }

            resultSet.close();
            statement.close();
            connection.close();
        } catch (Exception e) {
            e.printStackTrace();
            merchanttName.setText("Error retrieving merchant name");
        }
        
    }
